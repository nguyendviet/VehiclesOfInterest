    private boolean isFound(String field, String fieldType) {
        switch (fieldType) {
            case "make": 
                String[] allMakes = voiControl.allVehicleMakeKey();
                if (Arrays.asList(allMakes).contains(field)) {
                    return true;
                }
                return false;
            case "model":
                String[] allModels = voiControl.allVehicleModelKey();
                if (Arrays.asList(allModels).contains(field)) {
                    return true;
                }
                return false;
            case "reason":
                String[] allReasons = voiControl.allReasonsForInterestKey();
                if (Arrays.asList(allReasons).contains(field)) {
                    return true;
                }
                return false;
            case "wanted":
                String[] allWantedPlates = voiControl.allVehicleOfInterestKey();
                if (Arrays.asList(allWantedPlates).contains(field)) {
                    return true;
                }
                return false;
            default: return false;
        }
    }

