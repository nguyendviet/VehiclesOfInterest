    private void AddVehicleOfInterestButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddVehicleOfInterestButtonActionPerformed
        // Required fields
        String plate = NewVehicleOfInterestPlateText.getText().trim();
        String reason = NewVehicleOfInterestReasonText.getText().trim();
        String make = NewVehicleOfInterestMakeText.getText().trim();
        String model = NewVehicleOfInterestModelText.getText().trim();
        
        if (plate.isEmpty() || reason.isEmpty() || make.isEmpty() || model.isEmpty()
                || plate.equals(PLATE_PLACEHOLDER) || reason.equals(REASON_PLACEHOLDER)
                || make.equals(MAKE_PLACEHOLDER) || model.equals(MODEL_PLACEHOLDER)) {
            String message = "All required fields must be filled!";
            alert("Warning", message, 300, 100);
            return;
        }
        
        // Optional fields
        String year = NewVehicleOfInterestYearText.getText().trim();
        String color = NewVehicleOfInterestColorText.getText().trim();
        String ownerName = NewVehicleOfInterestOwnerNameText.getText().trim();
        String ownerPhone = NewVehicleOfInterestOwnerPhoneText.getText().trim();
        
        if (year.equals(YEAR_PLACEHOLDER)) {
            year = "";
        }
        
        if (color.equals(COLOR_PLACEHOLDER)) {
            color = "";
        }
        
        if (ownerName.equals(OWNER_NAME_PLACEHOLDER)) {
            ownerName = "";
        }
        
        if (ownerPhone.equals(OWNER_PHONE_PLACEHOLDER)) {
            ownerPhone = "";
        }
        
        try {
            if (!isFound(make, "make")) {
                voiControl.createVehicleMake(make);
            }
            
            if (!isFound(model, "model")) {
                voiControl.createVechileModel(model, make);
            }
            
            if (!isFound(reason, "reason")) {
                voiControl.createReasonOfInterest(reason, "N/A");
            }
            
            if (isFound(plate, "wanted")) {
                voiControl.updateVehicleOfInterest(plate, reason, make, model, 
                        year, color, ownerName, ownerPhone);
                String message = "Vehicle: " + plate + " already of interest."
                    + " Details updated.";
                alert("Notice", message, 800, 100);
                return;
            }
            
            voiControl.createVehicleOfInterest(plate, reason, make, model, year, 
                    color, ownerName, ownerPhone);
            
            String message = "Vehicle: " + plate + " added.";
            alert("Success", message, 300, 100);
        } catch(Exception e) {
            String message = "Vehicle: " + plate 
                    + " cannot be added."
                    + " Reason: " + e.getMessage();
            alert("Warning", message, 800, 100);
        }
    }//GEN-LAST:event_AddVehicleOfInterestButtonActionPerformed

