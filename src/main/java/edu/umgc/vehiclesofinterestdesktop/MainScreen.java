    private void AddNewModelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddNewModelButtonActionPerformed
        String newMake = NewMakeText.getText().trim();
        String newModel = NewModelText.getText().trim();
        
        if (newMake.isEmpty() || newMake.equals(MAKE_PLACEHOLDER) 
                || newModel.isEmpty() || newModel.equals(MODEL_PLACEHOLDER)) {
            String message = "Both Model and Make are required!";
            alert("Warning", message, 300, 100);
        }
        
        if (!newMake.isEmpty() && !newModel.isEmpty()) {
            if (isFound(newMake, "make") && isFound(newModel, "model")) {
                String message = newMake + " " + newModel + " already exists.";
                alert("Notice", message, 300, 100);
                return;
            }
            
            // If the new make doesn't exist, add it to the table
            if (!isFound(newMake, "make")) {
                voiControl.createVehicleMake(newMake);
            }
            voiControl.createVechileModel(newModel, newMake);
            
            String message = newMake + " " + newModel + " added!";
            alert("Success", message, 300, 100);
        }
    }//GEN-LAST:event_AddNewModelButtonActionPerformed

